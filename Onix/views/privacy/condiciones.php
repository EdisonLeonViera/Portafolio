            <p>Al hacer uso de la App Web usted acepta su politca de privacidad, esta politca es regida por la acltual ley chilena sobre el uso y proteccion de datos.</p>

            <h4>Se adjunta la ley N° 19.628</h4>

            <p style="text-align:justify">
                La Ley N° 19.628 regula el trato de los datos de carácter personal, en registros o bancos de datos, por organismos públicos o privados, y es uno de los estatutos normativos más relevantes sobre la materia.
            </p>
            <p style="text-align:justify">
                Actualmente existe consenso en la justificación del almacenamiento de datos personales, principalmente debido a que "la existencia de información suficiente es fundamental para el funcionamiento de los mercados"2. Pero es importante que esto vaya de la
                mano con el desarrollo del principio de protección de datos y el de autodeterminación informativa de las personas. El primero en los últimos tiempos ha tomado el rango de Derecho Fundamental autónomo, y supone "una restricción a su colecta, tratamiento
                y difusión"3. Por su parte, el segundo "consiste en la posibilidad que tiene el titular de los datos personales de controlar quienes sean destinatarios de éstos y qué uso le darán, y se ejercita genéricamente a través de los derechos de acceso, rectificación
                y cancelación"4, la autodeterminación informativa se levanta como un principio que establece criterios con los cuales los titulares permitirán el manejo de datos, tal como se verá, resultará fundamental el consentimiento5 en estos casos.
            </p>
            <p style="text-align:justify">
                Ahora, una de las aristas más importantes de esta ley es la que señala su artículo 16, el procedimiento6, que debe llevarse a efecto en caso de solicitud de modificación de datos, cancelación, bloqueo o eliminación de las bases de datos de información
                que se encuentra caduca o carece de fundamento legal (art. 12 inc. 3 LPVP). Pero el procedimiento específicamente creado para las controversias que se susciten sobre los supuestos planteados por esta ley ha resultado prácticamente inutilizado ante
                las tentaciones del recurso de protección, acción que se ha mostrado como una efectiva herramienta ante la publicación de información comercial carente de sustento legal. Así, en la mayoría de las acciones de protección que existen sobre la materia,
                aunque se alegue recurrentemente que no es el procedimiento correcto, ya que existe uno especial y legalmente establecido para tales efectos, se puede leer:
            </p>
            <p style="text-align:justify">
                "La acción de protección se puede ejercer sin perjuicio de otros derechos", o como lo dice la Corte Suprema: "cabe señalar que la existencia de un procedimiento especial contemplado en la Ley N° 19.628 no obsta al ejercicio de la acción de protección,
                porque ésta puede ejercerse sin perjuicio de otros derechos"7.
            </p>
            <p style="text-align:justify">
                Por lo antes expuesto, este trabajo se ceñirá específicamente a los casos de recursos de protección que se han tramitado, por considerar los recurrentes vulnerados sus derechos constitucionales y los emanados de la LPVP.
            </p>
