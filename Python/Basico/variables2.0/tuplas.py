tupla = tuple(["dato1","dato2"])
print(type(tupla))
print(tupla)

#ambas son tuplas con los datos separados con coma
tupla = "dato1","dato2"
print(type(tupla))
print(tupla)

#sigue siendo una tupla sin parentesis con un solo dato, para esto se le debe agregar una coma al final
tupla = "dato1",
print(type(tupla))
